int	ft_sqrt(int nb)
{
	int	x;

	x = 1;
	while (x * x <= nb)
	{
		if (x >= 463431)
		{
			return (0);
		}
		if (x * x == nb)
		{
			return (x);
		}
		x++;
	}
	return (0);
}
