#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int	k;

	k = 0;
	while (str[k] != '\0')
	{
		if ((str[k] >= 'a') && (str[k] <= 'z'))
			str[k] -= 'a' - 'A';
		k++;
	}
	return (str);
}
