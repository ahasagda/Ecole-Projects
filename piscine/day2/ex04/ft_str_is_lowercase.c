#include <unistd.h>

int	ft_str_is_lowercase(char *str)
{
	int	h;

	h = 0;
	while (str[h] != '\0')
	{
		if ((str[h] < 'a') || (str[h] > 'z'))
			return (0);
		h++;
	}
	return (1);
}
