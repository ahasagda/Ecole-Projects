#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	h;

	h = 0;
	while (str[h] != '\0')
	{
		if ((str[h] < '0') || (str[h] > '9'))
			return (0);
		h++;
	}
	return (1);
}
